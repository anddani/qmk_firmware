#define LOWER MO(_LOWER)
#define RAISE MO(_RAISE)

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (record->event.pressed) {
    switch(keycode) {
    case M_SHRG:
      /* ¯\_(ツ)_/¯ */
      send_unicode_hex_string("00AF 005C 005F 0028 30C4 0029 005F 002F 00AF");
      break;
    case M_FLIP_TABLE:
      /* (ノಠ益ಠ)ノ彡┻━┻ */
      send_unicode_hex_string("0028 30CE 0CA0 76CA 0CA0 0029 30CE 5F61 253B 2501 253B");
      break;
    case M_BOW:
      /* ＿|￣|● */
      send_unicode_hex_string("FF3F 007C FFE3 007C 25CF");
      break;
    case M_LNNY:
      /* ( ͡° ͜ʖ ͡°) */
      send_unicode_hex_string("0028 0020 0361 00B0 0020 035C 0296 0020 0361 00B0 0029");
      break;
    case M_PRAISE_THE_SUN:
      /* ( ﾉ ﾟｰﾟ)ﾉ☀️*/
      send_unicode_hex_string("0028 0020 FF89 0020 FF9F FF70 FF9F 0029 FF89 2600 FE0F");
      break;
    case M_SLTE:
      /* (￣^￣)ゞ */
      send_unicode_hex_string("0028 FFE3 005E FFE3 0029 309E");
      break;
    case M_GOOD:
      SEND_STRING("Hello everyone!! I wish you all good luck and have fun! /Mädchen");
      break;
    case M_MAC:
       set_unicode_input_mode(UC_OSX);
      break;
    case M_NIX:
       set_unicode_input_mode(UC_LNX);
      break;
    case M_WIN:
       set_unicode_input_mode(UC_WINC);
      break;
    }
  }
  return true;
};

